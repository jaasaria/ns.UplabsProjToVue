import Player from './Player'

export default {
	created() {
	},
	data() {
		return {
			headerHeight: 200,
			stretchyHeight: this.headerHeight
		}
	},
	methods: {
		 onScroll() {
				let parallaxSpeed = 0.33;
				let topView = this.$refs.topView.nativeView
				let scrollView = this.$refs.scrollView.nativeView

				const offset = scrollView.verticalOffset * parallaxSpeed * -1;

        if (scrollView.ios) {
            if (scrollView.verticalOffset < 0) {
                topView.translateY = Math.floor(0);
                this.stretchyHeight = this.headerHeight - scrollView.verticalOffset;
            } else {
                this.stretchyHeight = this.headerHeight;
                topView.translateY = Math.floor(offset);
            }
        }

        if (scrollView.android) {
            if (scrollView.verticalOffset < 0) {
                topView.translateY = Math.floor(0);
            } else {
                topView.translateY = Math.floor(offset);
            }
        }

    },
		onItemTap(){
			this.$navigateTo(Player,{
				animated: true,
				transition: {
					name: "slideLeft",
					duration: 250,
					curve: "easeIn"
				}
			})
		}
	},
	
